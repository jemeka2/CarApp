public class Vehicle {
    private int speed;
    private String car;

    public Vehicle(){

    }

    public void setSpeed(int speed){
        this.speed = speed;
    }
    public int getSpeed() {
        return speed;
    }

    public void setCar(String car){
        this.car = car;
    }
    public String getCar(){
        return this.car;
    }
}
